    private void update() {
        var sem = SharedVariables.getAll().values()
            .stream()
            .filter(x -> x.value instanceof Semaphore)
            .map(x -> (Semaphore) x.value)
            .toList();

        var model = new DefaultTableModel();
        model.setDataVector(
            sem.stream()
                .map(x -> new String[] { x.getName(), String.valueOf(x.getCapacity()), x.getBlockedPids().toString() })
                .collect(Collectors.toList())
                .toArray(new String[0][0]), 
            new String[] { "Sem√°foros", "Capacidad", "Bloqueados" }
        );
        semaphoresTable.setModel(model);
    }

