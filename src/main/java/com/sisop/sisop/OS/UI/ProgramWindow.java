    private void onSourceCodeButtonClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onSourceCodeButtonClicked
        var process = scheduler.get(pid);
        if (process != null) {
            String sourceCode = process.getInterpreter()
                                    .getContext()
                                    .getProgram()
                                    .getSourceCode();

            var frame = new SourceCodeFrame(sourceCode);
            frame.setVisible(true);
        }
    }//GEN-LAST:event_onSourceCodeButtonClicked


